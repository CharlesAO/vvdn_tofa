int main (int argc, char *argv[])
{
    UNUSED (argc);
    UNUSED (argv);
    u32    sippErrorLog[SIPP_ERROR_HISTORY_SIZE];

    #if defined(SIPP_VCS)
    unitTestInit ();
    DrvCprStartAllClocks ();
    DrvCprInit ();
    DrvTimerInit ();
    DrvLL2CInitWriteThrough ();
    // Need this with current heap init code which will attempt to init the DDR heap on setup
    DrvDdrInitialise (NULL);

    // Initialise the cmx dma driver
    #ifdef USE_CMX_DMA_NEW_DRIVER
    DrvCmxDmaInitialize ((DrvCmxDmaSetupStruct *)NULL);
    #else
    DrvCmxDmaInitDefault ();
    #endif
    #endif

    // Bypass CMXDMA SW reset
    sippSetInitMask (0x0);

    if (sippInitialize () == FALSE)
    {
        // Hard core kill as this function is for test prototyping only
        exit (0x0);
    }

    if (PROCESS_LEON_OS == swcWhoAmI())
    {
        printf ("App Starting on LEON OS\n");
        printf ("Please make sure IRQ sources for dynamically route between LeonOS and LeonRT are set\n");
    }
    else
    {
        printf ("App Starting on LEON RT\n");
    }

    #ifndef SIPP_TEST_SYNC_API
    // Enable interrupts
    swcLeonSetPIL (0);
    #endif

    // Paint slice assignments with known value
    for (uint32_t volatile * i = (uint32_t volatile *)(BASE_ADDRESS + SLICE_SIZE * SIPP_TEST_FIRST_SLICE); i < (uint32_t volatile *)(BASE_ADDRESS + SLICE_SIZE * (SIPP_TEST_LAST_SLICE + 0x1)); i++)
    {
        *i = 0x1337beef;
    }

    buildMemRegionApi (&myPl);

    if (sippPipeGetErrorStatus (myPl.pl))
    {
        u32 errNum;
        printf ("Pipeline creation error\nError codes:\n");
        errNum = sippGetErrorHistory (sippErrorLog);
        while (errNum)
        {
            printf ("%08lx\n", sippErrorLog[errNum - 0x1]);
            errNum--;
        }
    }

    #ifndef SIPP_TEST_SYNC_API
    // Register callback
    sippRegisterEventCallback (myPl.pl,
                               appSippCallback);
    #endif


    configMemRegionApi (&myPl);

    #ifdef DISABLE_OPIPE_CONNECTIONS
    myPl.pl->flags |= (PLF_DISABLE_OPIPE_CONS);
    #endif

    // Set a flag to enable consideration of oPipe runtime (that which we wish to use here)
    myPl.pl->flags |= PLF_CONSIDER_OPIPE_RT;

    sippChooseMemPool (&myPl.pl->tHeapMCB,
                       vPoolScheduleTemp, 1);

    #ifdef USE_MEM_LINE_BUFFER_API
    sippAllocCmxMemRegion (myPl.pl,
                           CmxMemRegions);
    #else
    // For fairness of comparison
    sippFinalizePipeline (myPl.pl);
    #endif

    readMemRegionApiInput (&myPl);

    #if defined(SIPP_VCS)
    DrvTimerStartTicksCount (&timer_data);
    DrvTimerGetElapsedTicks (&timer_data, &cycles_elapsed);
    #endif

    #ifdef SIPP_TEST_SYNC_API
    sippProcessFrame (myPl.pl);
    #else
    sippProcessFrameNB(myPl.pl);
    while ( testComplete == 0x0 )
    {

    }

    #endif

    if (sippPipeGetErrorStatus (myPl.pl))
    {
        u32 errNum;
        printf ("Pipeline runtime error\nError codes:\n");
        errNum = sippGetErrorHistory (sippErrorLog);
        while (errNum)
        {
            printf ("%08lx\n", sippErrorLog[errNum - 0x1]);
            errNum--;
        }
    }

    #if defined(SIPP_VCS)
    // Count cycles elapsed
    DrvTimerGetElapsedTicks (&timer_data, &cycles_elapsed);
    printf ("Cycles: %llu\n", cycles_elapsed);
    #endif

    writeMemRegionApiOutput (&myPl);

    sippRdFileU8(MemRegionApi_dmaTest_buffer, MEM_API_TEST_BAYER_WIDTH* MEM_API_TEST_BAYER_HEIGHT* 3, "../../../../../../resources/test_522x128_RGB.raw");
    sippDbgCompareU8(MemRegionApi_dmaTest_buffer, MemRegionApi_dmaOut0_buffer, MEM_API_TEST_BAYER_WIDTH* MEM_API_TEST_BAYER_HEIGHT* 3);

    #ifdef CHECK_TOUCHED_REGION
    int on_track = 1;
    uint32_t volatile * j;
    for (j = (uint32_t volatile *) (BASE_ADDRESS + SLICE_SIZE * SIPP_TEST_FIRST_SLICE);
            j < (uint32_t volatile *) (BASE_ADDRESS + SLICE_SIZE * (SIPP_TEST_LAST_SLICE + 0x1)); j++)
    {
        if (*j != 0x1337beef)
        {
            if (on_track)
            {
                printf("%p to ", j);
                on_track = 0;
            }
        }
        else
        {
            if (!on_track)
            {
                printf("%p\n", j - 1);
                on_track = 1;
            }
        }
    }

    if (!on_track)
    {
        printf("%p\n", j - 1);
    }
    #endif

    // Check that slices used by the pipe are not overwritten
    uint32_t volatile * slice;
    for (slice = (uint32_t volatile *) (BASE_ADDRESS + SLICE_SIZE * SIPP_PIPE_START_SLICE);
            slice < (uint32_t volatile *) (BASE_ADDRESS + SLICE_SIZE * (SIPP_PIPE_END_SLICE + 0x1)); slice += SLICE_INC)
    {
        unitTestCrcCheck((void *)slice, SLICE_SIZE, expectedCRC);
    }

    unitTestFinalReport ();
    return 0;
}
